public class CryingObsidianBlock extends Block {
    public CryingObsidianBlock(Properties pProperties) {
        super(pProperties);
    }

    @Override
    public InteractionResult use(BlockState pState, Level pLevel, BlockPos pPos,
                                 Player pPlayer, InteractionHand pHand, BlockHitResult pHit) {

        pLevel.playSound(null, pPos, SoundEvents.RESPAWN_ANCHOR_SET_SPAWN, SoundSource.BLOCKS, 1.0F, 1.0F);

        if (!pLevel.isClientSide && pPlayer instanceof ServerPlayer) {

            ServerPlayer serverPlayer = (ServerPlayer) pPlayer;

            serverPlayer.setRespawnPosition(pLevel.dimension(), pPos.above(), 0.0F, true, true);


            //System.out.println("Respawn Position: " + getRespawnPosition() + ", Dimension: " + respawnDimension);

        }
            return InteractionResult.SUCCESS;

        }
}

